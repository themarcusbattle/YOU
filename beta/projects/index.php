	<h3>How to manage You</h3>
	<ol>	
		<li>Define the places you frequent (May be physical or mental)</li>
		<li>Define who you are in these places (Husband, Web Dev, Elder, Programmer) This changes at times</li>
		<li>Define any specific projects you're working on there (Campus Visits, Build Car)</li>
		<li>Define those loose ends that always come up and disrupt your projects. This can be defined by a superior or relative to your perspective <i>aka assumed roles</i> (washing the car, doing the laundry, answering the phone, paying the bills)</li>
	</ol>
	<h4>Notes</h4>
	<p>Please note that you operate differently in all of these places</p>
	<p>Who you are and what you do is different at times</p>
	<p>Somethings you do are definite, with an actual end date. Others are ongoing, such as washing a car or maintaining a website. It's these ongoing projects that build and seem to consume us, because we don't know how to give them definition.</p>
	<p>There are those unique times in your life, when a project you have poured into will become a "place" - a world separate from any other.</p>
	<p>It's possible that an area of your life that has no recent project or ongoing tasks, is not getting the attention it deserves. Or it you deem that it deserves no attention, then that place has become irrelivant.</p>
	<p><span class="label label-success">Suggestion</span> To bring structure to your life and redeem time, attempt to automate areas in the "Ongoing" category. Find unique ways to delegate, or save time with these areas.</p>
	<p><span class="label label-success">Suggestion</span> Clarify your biggest hurdles in the "Ongoing" section. This can be the start of you proactively processing and clearing the stress of this area.</p>
	<p><span class="label label-info">Note</span> Ongoing items are often sent as urgent, ASAP or before the day is out. Projects are important because without your skillset beign applied to it, the project won't get completed in a timely manner. Projects have a definite end so take time and complete them. Deferentiating between ongoing and projects is the art of learning what's important versus what's urgent.</p>
